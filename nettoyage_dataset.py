    "def adjust_dimensions(wb, max_column_width=50):\n",
    "    # Adjust column widths\n",
    "    for ws in wb.worksheets:\n",
    "        # Freeze the first row and the first column\n",
    "        ws.freeze_panes = ws['B2']  # Freezes everything above and to the left of B2 (i.e., row 1 and column 1)\n",
    "        \n",
    "        column_lengths = {}\n",
    "        for row in ws.iter_rows():\n",
    "            for cell in row:\n",
    "                if cell.value is not None:\n",
    "                    # Center align the cell content and enable text wrapping\n",
    "                    cell.alignment = Alignment(horizontal='center', vertical='center', wrap_text=True)\n",
    "                    \n",
    "                    column_letter = get_column_letter(cell.column)\n",
    "                    if column_letter not in column_lengths:\n",
    "                        column_lengths[column_letter] = 0\n",
    "                    cell_length = len(str(cell.value))\n",
    "                    if cell_length > column_lengths[column_letter]:\n",
    "                        column_lengths[column_letter] = cell_length\n",
    "\n",
    "        for col, length in column_lengths.items():\n",
    "            # Set the width but cap it at the max_column_width\n",
    "            if length + 2 > max_column_width:\n",
    "                ws.column_dimensions[col].width = max_column_width\n",
    "            else:\n",
    "                ws.column_dimensions[col].width = length + 2  # Add buffer for padding\n",
    "\n",
    "        # Adjust row heights considering text wrapping\n",
    "        row_heights = {}\n",
    "        for row in ws.iter_rows():\n",
    "            row_number = row[0].row\n",
    "            max_height = 0\n",
    "            for cell in row:\n",
    "                if cell.value is not None:\n",
    "                    cell_value = str(cell.value)\n",
    "                    # Estimate the number of lines after wrapping\n",
    "                    wrapped_text = cell_value.splitlines()\n",
    "                    line_count = 0\n",
    "                    for line in wrapped_text:\n",
    "                        # Calculate how many lines this piece of text would occupy\n",
    "                        wrapped_lines = max(1, (len(line) // max_column_width) + 1)\n",
    "                        line_count += wrapped_lines\n",
    "                    if line_count > max_height:\n",
    "                        max_height = line_count\n",
    "            row_heights[row_number] = max_height * 15  # Approximate row height based on line count\n",
