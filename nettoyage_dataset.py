    "def rename_columns(df):\n",
    "    \"\"\"\n",
    "    Renames the columns of the given DataFrame based on the provided correspondence dictionary.\n",
    "\n",
    "    Parameters:\n",
    "    df (pd.DataFrame): The DataFrame whose columns need to be renamed.\n",
    "    correspondance_dict (dict): A dictionary where keys are original column names and values are the new column names.\n",
    "\n",
    "    Returns:\n",
    "    pd.DataFrame: The DataFrame with updated column names.\n",
    "    \"\"\"\n",
    "    # Initialize an empty list to store the new column names\n",
    "    new_columns = []\n",
    "    \n",
    "    # Iterate over the columns of the DataFrame\n",
    "    for col in df.columns:\n",
    "        # If the column name is in the dictionary, use the corresponding new name\n",
    "        if col in correspondance_dict:\n",
    "            new_columns.append(correspondance_dict[col])\n",
    "        else:\n",
    "            # Otherwise, keep the original column name\n",
    "            new_columns.append(col)\n",
    "    \n",
    "    # Assign the new column names to the DataFrame\n",
    "    df.columns = new_columns\n",
    "    \n",
    "    # Return the DataFrame with the updated column names\n",
    "    return df\n",
    "\n",
