    "def correct_and_save_columns(df, output_file_name):\n",
    "    \"\"\"\n",
    "    Swaps every two adjacent columns and updates their names according to a specific pattern,\n",
    "    then saves the corrected DataFrame to a CSV file.\n",
    "\n",
    "    Parameters:\n",
    "    df (pd.DataFrame): The DataFrame whose columns need to be corrected.\n",
    "    output_file (str): The name of the output CSV file to save the corrected DataFrame.\n",
    "\n",
    "    Returns:\n",
    "    pd.DataFrame: The DataFrame with corrected column names.\n",
    "    \"\"\"\n",
    "    # Get the original column names as a list\n",
    "    original_columns = df.columns.tolist()\n",
    "    \n",
    "    # Create a copy of the original column names to modify\n",
    "    corrected_columns = original_columns.copy()\n",
    "    \n",
    "    # Swap every two adjacent column names\n",
    "    for i in range(0, len(original_columns), 2):\n",
    "        if i + 1 < len(original_columns):\n",
    "            corrected_columns[i], corrected_columns[i + 1] = original_columns[i + 1], original_columns[i]\n",
    "    \n",
    "    # Update the names of every two adjacent columns\n",
    "    for i in range(0, len(corrected_columns), 2):\n",
    "        if i + 1 < len(corrected_columns):\n",
    "            corrected_columns[i] = f'Dates for {corrected_columns[i + 1]}'\n",
    "    \n",
    "    # Set the corrected column names to the DataFrame\n",
    "    df.columns = corrected_columns\n",
    "    \n",
    "    # Save the corrected DataFrame to a new CSV file\n",
    "    df.to_csv(f\"{output_file_name}.csv\", index=False)\n",
    "    return df\n",
    "\n",
