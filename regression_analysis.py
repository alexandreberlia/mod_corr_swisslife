    "    merged_df = pd.concat([std_1, std_2], axis=1)\n",
    "    merged_df = merged_df.T.groupby(merged_df.T.index).sum().T\n",
    "    replacements = {3:2, -3:-2}\n",
    "    merged_df = merged_df.replace(replacements)\n",
    "    merged_df = merged_df.transpose()\n",
