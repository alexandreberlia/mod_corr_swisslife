    "def retain_5_highest_and_lowest(std_limit, variable_to_apply_regression_on, save_excel='no', cell_width=50, excel_name=\"5 Highest and Lowest Coefficients\"):\n",
    "    df = lin_regression_stats_model(std_limit=std_limit, variable_to_apply_regression_on=variable_to_apply_regression_on)\n",
    "    \n",
    "    # Extract the coefficients table from the summary object\n",
    "    coeff_table = df.tables[1]\n",
    "    \n",
    "    # Extract rows from the coefficient table and convert them into a DataFrame\n",
    "    coeff_data = []\n",
    "    for row in coeff_table.data[1:]:  # Skip the header row\n",
    "        coeff_data.append([row[0], float(row[1])])  # Assuming 'coef' is the second column\n",
    "\n",
    "    coeff_df = pd.DataFrame(coeff_data, columns=['Variable', 'coef'])\n",
    "    \n",
    "    # Sort by the coef column to get the highest and lowest values\n",
    "    sorted_coeff_df = coeff_df.sort_values(by='coef')\n",
