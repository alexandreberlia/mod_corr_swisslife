    "def all_variables_highest_lowest(std_limit):\n",
    "    x = filter_the_original_df(std_limit=std_limit)\n",
    "    data = []\n",
    "    for variable in x.columns:\n",
    "        data.append(retain_5_highest_and_lowest(std_limit=std_limit, variable_to_apply_regression_on=variable))\n",
    "    transformed_data = []\n",
    "\n",
    "# Iterate over each element in the list of lists\n",
    "    for entry in data:\n",
    "        variable = entry[0]\n",
    "        corresponding_variables = entry[1]\n",
    "        anti_variables = entry[2]\n",
    "        \n",
    "        # Iterate over corresponding variables and append to the transformed data\n",
    "        for var, value in corresponding_variables:\n",
    "            transformed_data.append([variable, var, value])\n",
    "        \n",
    "        # Iterate over anti-variables and append to the transformed data\n",
    "        for var, value in anti_variables:\n",
    "            transformed_data.append([variable, var, value])\n",
    "\n",
    "    # Convert the transformed data into a DataFrame\n",
    "    df = pd.DataFrame(transformed_data, columns=[\"Variable\", \"SubVariable\", \"Correlation Value\"])\n",
    "    df.set_index(\"Variable\", inplace=True)\n",
    "    return df\n",
