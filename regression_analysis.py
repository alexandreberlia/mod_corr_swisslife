    "def lin_regression_stats_model(std_limit, variable_to_apply_regression_on, save_excel='no', cell_width=50, excel_name=\"Linear Regression Stats\"):\n",
    "    linear_regression_df = filter_the_original_df(std_limit=std_limit)\n",
    "    Y = linear_regression_df[variable_to_apply_regression_on]\n",
    "    variables_list = []\n",
    "    for i in range(len(linear_regression_df.columns)):\n",
    "        if variable_to_apply_regression_on == linear_regression_df.columns[i]:\n",
    "            continue\n",
    "        else:\n",
    "            variables_list.append(linear_regression_df.columns[i])\n",
    "    X = linear_regression_df[variables_list]\n",
    "    X = sm.add_constant(X)\n",
    "    model = sm.OLS(Y, X).fit()\n",
    "    #print(model.summary())\n",
    "    summary_stats = model.summary2().tables[0]  # Model statistics\n",
    "    summary_table = model.summary2().tables[1]  # Summary2 has a cleaner table format\n",
    "    combined_summary = pd.concat([summary_stats, summary_table], axis=0)\n",
    "    if save_excel == 'yes':\n",
    "        combined_summary.to_excel(f\"{excel_name}.xlsx\")\n",
    "        wb = load_workbook(f\"{excel_name}.xlsx\")\n",
    "        \n",
    "        # Adjust dimensions automatically\n",
    "        adjust_dimensions(wb, max_column_width=cell_width)\n",
    "        \n",
    "        # Save the workbook with the adjusted dimensions\n",
    "        wb.save(f\"{excel_name}.xlsx\")\n",
    "\n",
    "    return model.summary()\n",
